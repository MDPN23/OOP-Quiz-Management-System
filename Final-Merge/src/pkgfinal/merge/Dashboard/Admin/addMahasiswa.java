    private String getSelectedGender() {
        if (genderLakiLaki.isSelected()) {
            return "Laki-Laki";
        } else if (genderPerempuan.isSelected()) {
            return "Perempuan";
        } else {
            return ""; // Atau dapat diatasi sesuai kebutuhan, misalnya dengan mengembalikan null atau nilai default lainnya.
        }
    }
    
